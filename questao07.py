'''
Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números.
'''

num1 = float(input("Informe o 1º número: "))
num2 = float(input("Informe o 2º número: "))
num3 = float(input("Informe o 3º número: "))

media = (num1 + num2 + num3) / 3

print("A média aritmética entre estes três números é de: ", media)
