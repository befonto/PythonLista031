'''
Elaborar um programa que pergunte quatro valores inteiros e apresente 2 resultados:
'''

a = int(input("Dê um valor ao 1º número: "))
b = int(input("Dê um valor ao 2º número: "))
c = int(input("Dê um valor ao 3º número: "))
d = int(input("Dê um valor ao 4º número: "))

# A
print("A soma dos seus quatro números será de", a + b + c + d,)

# B
print("A multiplicação dos seus quatro números será de", a * b * c * d,)